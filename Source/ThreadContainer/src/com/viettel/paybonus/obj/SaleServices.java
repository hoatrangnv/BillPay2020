package com.viettel.paybonus.obj;

/**
 *
 * @author itbl_linh
 */
public class SaleServices implements java.io.Serializable {

    private Long saleServicesId;
    private String name;
    private Long status;
    private String code;
    private String accountModelCode;
    private String accountModelName;

    public Long getSaleServicesId() {
        return saleServicesId;
    }

    public void setSaleServicesId(Long saleServicesId) {
        this.saleServicesId = saleServicesId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getStatus() {
        return status;
    }

    public void setStatus(Long status) {
        this.status = status;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getAccountModelCode() {
        return accountModelCode;
    }

    public void setAccountModelCode(String accountModelCode) {
        this.accountModelCode = accountModelCode;
    }

    public String getAccountModelName() {
        return accountModelName;
    }

    public void setAccountModelName(String accountModelName) {
        this.accountModelName = accountModelName;
    }
}
