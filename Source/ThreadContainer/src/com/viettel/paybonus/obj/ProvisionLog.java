package com.viettel.paybonus.obj;

public class ProvisionLog {

    private String msisdn;
    private String request;
    private String respone;
    private long duration;
    private String processCode;
    private String responeCode;

    public ProvisionLog(String msisdn, String request, String respone, 
            long duration, String processCode, String responeCode) {
        this.msisdn = msisdn;
        this.duration = duration;
        this.processCode = processCode;
        this.request = request;
        this.respone = respone;
        this.responeCode = responeCode;
    }

    public ProvisionLog() {
    }

    public String getResponeCode() {
        return this.responeCode;
    }

    public void setResponeCode(String responeCode) {
        this.responeCode = responeCode;
    }

    public long getDuration() {
        return this.duration;
    }

    public void setDuration(long duration) {
        this.duration = duration;
    }

    public String getMsisdn() {
        return this.msisdn;
    }

    public void setMsisdn(String msisdn) {
        this.msisdn = msisdn;
    }

    public String getProcessCode() {
        return this.processCode;
    }

    public void setProcessCode(String processCode) {
        this.processCode = processCode;
    }

    public String getRequest() {
        return this.request;
    }

    public void setRequest(String request) {
        this.request = request;
    }

    public String getRespone() {
        return this.respone;
    }

    public void setRespone(String respone) {
        this.respone = respone;
    }  
}