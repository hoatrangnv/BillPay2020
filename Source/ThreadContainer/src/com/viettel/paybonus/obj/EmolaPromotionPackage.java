package com.viettel.paybonus.obj;

/**
 *
 * @author dev_manhnv
 */
public class EmolaPromotionPackage {
    private long ranking;
    private long rankingMarked;
    private int promotionType;
    private String promotionSMS;
    private double bonusFloat;
    private String gift;

    public int getPromotionType() {
        return promotionType;
    }

    public void setPromotionType(int promotionType) {
        this.promotionType = promotionType;
    }

    public String getPromotionSMS() {
        return promotionSMS;
    }

    public long getRanking() {
        return ranking;
    }

    public void setRanking(long ranking) {
        this.ranking = ranking;
    }

    public void setPromotionSMS(String promotionSMS) {
        this.promotionSMS = promotionSMS;
    }

    public double getBonusFloat() {
        return bonusFloat;
    }

    public void setBonusFloat(double bonusFloat) {
        this.bonusFloat = bonusFloat;
    }

    public String getGift() {
        return gift;
    }

    public void setGift(String gift) {
        this.gift = gift;
    }

    public long getRankingMarked() {
        return rankingMarked;
    }

    public void setRankingMarked(long rankingMarked) {
        this.rankingMarked = rankingMarked;
    }

    @Override
    public String toString() {
        return "EmolaPromotionPackage{" + "ranking=" + ranking + ", promotionType=" + promotionType + ", promotionSMS=" + promotionSMS + ", bonusFloat=" + bonusFloat + ", gift=" + gift + '}';
    }

}
