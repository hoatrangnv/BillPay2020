package com.viettel.paybonus.obj;

/**
 *
 * @author kdvt_thuannh2
 */
public class ChargeLog {

    private String msisdn;
    private long subId;
    private int actionType;
    private String balanceId;
    private String balanceValue;
    private String dataName;
    private String description;

    public ChargeLog(String msisdn, long subId, String dataName, int actionType,
            String balanceId, String balanceValue, String description) {
        this.msisdn = msisdn;
        this.subId = subId;
        this.dataName = dataName;
        this.actionType = actionType;
        this.balanceId = balanceId;
        this.balanceValue = balanceValue;
        this.description = description;
    }

    public ChargeLog() {
    }

    public String getMsisdn() {
        return msisdn;
    }

    public void setMsisdn(String msisdn) {
        this.msisdn = msisdn;
    }

    public long getSubId() {
        return subId;
    }

    public void setSubId(long subId) {
        this.subId = subId;
    }

    public int getActionType() {
        return actionType;
    }

    public void setActionType(int actionType) {
        this.actionType = actionType;
    }

    public String getBalanceId() {
        return balanceId;
    }

    public void setBalanceId(String balanceId) {
        this.balanceId = balanceId;
    }

    public String getBalanceValue() {
        return balanceValue;
    }

    public void setBalanceValue(String balanceValue) {
        this.balanceValue = balanceValue;
    }

    public String getDataName() {
        return dataName;
    }

    public void setDataName(String dataName) {
        this.dataName = dataName;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
