package com.viettel.paybonus.obj;

import com.viettel.cluster.agent.integration.Record;
import java.util.Date;

/**
 *
 * @author Huynq13
 */
public class BankFileDetail implements Record {

    private long bankFileDetailId;
    private long bankFileInfoId;
    private String reference;
    private long valuePay;
    private long valueCom;
    private Date fileTime;
    private String transId;
    private String terminalId;
    private String terminalLocation;
    private String idBatch;
    private String threadName;
    private String nodeName;
    private String clusterName;
    private String resultCode;
    private String description;
    private Date importTime;

    public long getBankFileDetailId() {
        return bankFileDetailId;
    }

    public void setBankFileDetailId(long bankFileDetailId) {
        this.bankFileDetailId = bankFileDetailId;
    }

    public long getBankFileInfoId() {
        return bankFileInfoId;
    }

    public void setBankFileInfoId(long bankFileInfoId) {
        this.bankFileInfoId = bankFileInfoId;
    }

    public String getReference() {
        return reference;
    }

    public void setReference(String reference) {
        this.reference = reference;
    }

    public long getValuePay() {
        return valuePay;
    }

    public void setValuePay(long valuePay) {
        this.valuePay = valuePay;
    }

    public long getValueCom() {
        return valueCom;
    }

    public void setValueCom(long valueCom) {
        this.valueCom = valueCom;
    }

    public Date getFileTime() {
        return fileTime;
    }

    public void setFileTime(Date fileTime) {
        this.fileTime = fileTime;
    }

    public String getTransId() {
        return transId;
    }

    public void setTransId(String transId) {
        this.transId = transId;
    }

    public String getTerminalId() {
        return terminalId;
    }

    public void setTerminalId(String terminalId) {
        this.terminalId = terminalId;
    }

    public String getTerminalLocation() {
        return terminalLocation;
    }

    public void setTerminalLocation(String terminalLocation) {
        this.terminalLocation = terminalLocation;
    }

    @Override
    public String getID() {
        return "" + this.bankFileDetailId;
    }

    @Override
    public String getBatchId() {
        return this.idBatch;
    }

    @Override
    public void setBatchId(String batchId) {
        this.idBatch = batchId;
    }

    @Override
    public String getClusterName() {
        return this.clusterName;
    }

    @Override
    public void setClusterName(String clusterName) {
        this.clusterName = clusterName;
    }

    @Override
    public String getNodeName() {
        return this.nodeName;
    }

    @Override
    public void setNodeName(String nodeName) {
        this.nodeName = nodeName;
    }

    @Override
    public String getTheadName() {
        return this.threadName;
    }

    @Override
    public void setThreadName(String threadName) {
        this.threadName = threadName;
    }

    @Override
    public String getResultCode() {
        return this.resultCode;
    }

    @Override
    public void setResultCode(String resultCode) {
        this.resultCode = resultCode;
    }

    public String getIdBatch() {
        return idBatch;
    }

    public void setIdBatch(String idBatch) {
        this.idBatch = idBatch;
    }

    public Date getImportTime() {
        return importTime;
    }

    public void setImportTime(Date importTime) {
        this.importTime = importTime;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
