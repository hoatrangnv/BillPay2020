package com.viettel.vas.wsfw.object;

/**
 *
 * @author dev_manhnv
 */
public class Offer {
    private String Name;
    private String Id;
    private String Version;
    private String ExpDate;
    private String EffDate;
    private String IdMember;
    private String ListMember;
    private String State;
    private String RecurringDate;
    private String UpgradeOrNot;
    private String UpgradeTime;
    private String DowngradeTime;

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public String getId() {
        return Id;
    }

    public void setId(String Id) {
        this.Id = Id;
    }

    public String getVersion() {
        return Version;
    }

    public void setVersion(String Version) {
        this.Version = Version;
    }

    public String getExpDate() {
        return ExpDate;
    }

    public void setExpDate(String ExpDate) {
        this.ExpDate = ExpDate;
    }

    public String getEffDate() {
        return EffDate;
    }

    public void setEffDate(String EffDate) {
        this.EffDate = EffDate;
    }

    public String getIdMember() {
        return IdMember;
    }

    public void setIdMember(String IdMember) {
        this.IdMember = IdMember;
    }

    public String getListMember() {
        return ListMember;
    }

    public void setListMember(String ListMember) {
        this.ListMember = ListMember;
    }

    public String getState() {
        return State;
    }

    public void setState(String State) {
        this.State = State;
    }

    public String getRecurringDate() {
        return RecurringDate;
    }

    public void setRecurringDate(String RecurringDate) {
        this.RecurringDate = RecurringDate;
    }

    public String getUpgradeOrNot() {
        return UpgradeOrNot;
    }

    public void setUpgradeOrNot(String UpgradeOrNot) {
        this.UpgradeOrNot = UpgradeOrNot;
    }

    public String getUpgradeTime() {
        return UpgradeTime;
    }

    public void setUpgradeTime(String UpgradeTime) {
        this.UpgradeTime = UpgradeTime;
    }

    public String getDowngradeTime() {
        return DowngradeTime;
    }

    public void setDowngradeTime(String DowngradeTime) {
        this.DowngradeTime = DowngradeTime;
    }
}
