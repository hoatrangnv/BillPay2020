package com.viettel.vas.wsfw.object;

/**
 *
 * @author dev_manhnv
 */
public class QueryEwalletInfo {
    private String ResponseCode;
    private String ResponseMessage;
    private String Status;
    private Float Balance;
    private Float CommissionBalance;
    private String ChannelCode;
    private String CustomerCode;
    private String ProviderCode;

    public QueryEwalletInfo() {
    }

    public String getResponseCode() {
        return ResponseCode;
    }

    public void setResponseCode(String ResponseCode) {
        this.ResponseCode = ResponseCode;
    }

    public String getResponseMessage() {
        return ResponseMessage;
    }

    public void setResponseMessage(String ResponseMessage) {
        this.ResponseMessage = ResponseMessage;
    }

    public String getStatus() {
        return Status;
    }

    public void setStatus(String Status) {
        this.Status = Status;
    }

    public Float getBalance() {
        return Balance;
    }

    public void setBalance(Float Balance) {
        this.Balance = Balance;
    }

    public Float getCommissionBalance() {
        return CommissionBalance;
    }

    public void setCommissionBalance(Float CommissionBalance) {
        this.CommissionBalance = CommissionBalance;
    }

    public String getChannelCode() {
        return ChannelCode;
    }

    public void setChannelCode(String ChannelCode) {
        this.ChannelCode = ChannelCode;
    }

    public String getCustomerCode() {
        return CustomerCode;
    }

    public void setCustomerCode(String CustomerCode) {
        this.CustomerCode = CustomerCode;
    }

    public String getProviderCode() {
        return ProviderCode;
    }

    public void setProviderCode(String ProviderCode) {
        this.ProviderCode = ProviderCode;
    }
}
