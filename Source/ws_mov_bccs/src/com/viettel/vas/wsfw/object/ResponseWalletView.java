public class ResponseWalletView {

    private String ResponseCode;
    private String ResponseMessage;
    private String Balance;
    private String CommisionBlance;
    private String ChannelCode;

    public ResponseWalletView() {
    }

    public String getResponseCode() {
        return ResponseCode;
    }

    public void setResponseCode(String ResponseCode) {
        this.ResponseCode = ResponseCode;
    }

    public String getResponseMessage() {
        return ResponseMessage;
    }

    public void setResponseMessage(String ResponseMessage) {
        this.ResponseMessage = ResponseMessage;
    }

    public String getBalance() {
        return Balance;
    }

    public void setBalance(String Balance) {
        this.Balance = Balance;
    }

    public String getCommisionBlance() {
        return CommisionBlance;
    }

    public void setCommisionBlance(String CommisionBlance) {
        this.CommisionBlance = CommisionBlance;
    }

    public String getChannelCode() {
        return ChannelCode;
    }

    public void setChannelCode(String ChannelCode) {
        this.ChannelCode = ChannelCode;
    }
}
